name = 'Md Ahmodullah'
age = "24"
profession = 'Python Basic Batch 01'
instructor = 'Abdul Aouwal'

# string concatenation using comma.
print('Hi, everyone. I\'m ', name, '. I\'m ', age, '. I\'m a student of ', profession, '. My instructor is ', instructor,'.', sep='')

# string concatenation using arithmatic operator
print('Hi, everyone. I\'m ' + name + '. I\'m ' + age + '. I\'m a student of ' + profession + '. My instructor is ' + instructor + '.')

# formatted string: Too much easier to use.
print(f'Hi, everyone. I\'m {name}. I\'m {age}. I\'m a student of {profession}. My instructor is  {instructor}.')

# Raw string: to show file path
print(r"F:'\facebook\Camera")

# Escape Character: Backslash(\) in orange colour
print(f'I\'m {age}.')

# New line: Backslash n (\n)  AND # Tab Backslash t (\t)
print('Twinkle Twinkle Little Star \n\t How I wonder what you are! \n\t\t Up above the world so high. \n\t\t Like a diamond in the sky. \nTwinkle Twinkle Little Star \n\t How I wonder what you are!')



