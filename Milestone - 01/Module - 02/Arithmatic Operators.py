# Floor Division: works like (দশমিকের যাই থাকুক বাদ দিবে)
print('-------')
print(21 // 4)  # output = 5
print(22 // 4)  # output = 5
print(23 // 4)  # output = 5

# round: works like (5.5 থেকে এর উপরে গেলে round হয়ে 6 হবে। আর 5.5 এর নিচে গেলে round হয়ে 5 হবে।)
result1 = 21 / 4    # output = 5.25
result2 = 22 / 4    # output = 5.5
result3 = 23 / 4    # output = 5.75
print(result1, result2, result3)
print(round(result1))  # output = 5
print(round(result2))  # output = 6
print(round(result3))  # output = 6


